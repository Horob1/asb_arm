  AREA RESET, DATA, READONLY
  DCD 0x20000000
  DCD Main
LENGTH DCB 5
VECTOR_1 DCB 1,1,1,1,1
VECTOR_2 DCB 2,2,2,2,2

  AREA CODE, CODE, READONLY
  ENTRY
Main
  LDRB R0, LENGTH
  CMP R0, #0
  BEQ Stop

  LDR R1, =VECTOR_1
  LDR R2, =VECTOR_2
  MOV R6, #0

Loop
  LDRB R3, [R1], #1
  LDRB R4, [R2], #1
  MUL R5, R3, R4
  ADDS R6, R6, R5
  SUBS R0, R0, #1
  CMP R0, #0
  BNE Loop

Stop B Stop
  END